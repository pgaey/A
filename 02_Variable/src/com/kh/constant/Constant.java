package com.kh.constant;

public class Constant {

	// * 상수(수학)
	// 변하지 않는 값(고정된 값)
	
	public static void main(String[] args) {

		// 정수 (양수, 음수)
		System.out.println('7');	// 문자 7
		System.out.println("7");	// 문자열 7
		System.out.println(7);	    // 정수 7 
		System.out.println(-7);
		
		// 실수(.)
		System.out.println(0.7);
		System.out.println(-0.7);
		
		// (단일)문자, 문자열
		System.out.println('a');	// ''  반드시 단일 문자만 가능  ex) 'abcd' 는 출력되지 않음
		System.out.println("b");	// ""  몇 글자이든 상관 없음
		
		// 논리값(true, false)
		System.out.println(true);
		System.out.println(false);
		
	}

	
}
