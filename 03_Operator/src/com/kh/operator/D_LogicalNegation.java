package com.kh.operator;

public class D_LogicalNegation {

	/*
	 * 논리(값) 부정 연산자 : 논리값(true, false)을 반대로 바꿔주는 연산자
	 * 
	 * [ 표현법  ] !논리값;
	 */
	
	public void method1() {
		
		System.out.println(true);
		System.out.println(!true);
		System.out.println(!!false);
		System.out.println(!!!false);
		
		
		
		
		
		
	}

	
	
	
	
	
}
