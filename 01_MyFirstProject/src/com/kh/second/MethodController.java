package com.kh.second; // 패키지 선언부

public class MethodController { // MethodController 클래스 영역 시작

	public void methodA() {  // methodA 영역 시작 
		System.out.println("methodA 호출!!");
	}  // methodA 영역 끝
	
	public void printMyName() {
		System.out.println("내 이름이다");
	}
	
	public void methodB( ) {
		System.out.println("실습 : 메소드 호출하기2");
	}
	
	public void methodC( ) {
		System.out.println("강의실이 덥다");
	}

	
	
} // MethodController 클래스 영역 끝
